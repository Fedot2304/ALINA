function btn1(){

    let block = document.querySelector('#sub_first')
    if(block.classList.contains('visible')){
        block.classList.add('invisible')
        block.classList.remove('visible')
    } else{
        block.classList.add('visible')
        block.classList.remove('invisible')
    }

	let sec = document.querySelector('#sub_second')
    if(sec.classList.contains('visible')){
        sec.classList.add('invisible')
        sec.classList.remove('visible')
    }
	let three = document.querySelector('#sub_three')
    if(three.classList.contains('visible')){
        three.classList.add('invisible')
        three.classList.remove('visible')
    }
	let four = document.querySelector('#sub_four')
    if(four.classList.contains('visible')){
        four.classList.add('invisible')
        four.classList.remove('visible')
    }

/*
	let cont = document.querySelector('.conteiner')
    if(four.classList.contains('visible')){
        cont.classList.add('big')
    } else{
        cont.classList.remove('big')
	}*/
}



